export const getIdeas = (n: number) => {
  const shuffled = [...IDEAS].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, n);
};

export function useIdeas(n: number) {
  const query = useQuery(
    ['ideas', n],
    () => {
      return getIdeas(n);
    },
    {
      refetchOnMount: true,
      initialData: getIdeas(n),
    },
  );

  return query;
}
